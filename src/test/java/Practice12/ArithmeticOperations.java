package Practice12;

public class ArithmeticOperations {

	public void add() {
		int a = 300 ;
		int b = 30 ;
		int c = a + b ;
		System.out.println(c);
	}
	public void subract() {
		int d = 400;
		int e = 40;
		int f = d - e ;
		System.out.println(f);
	}
	public void multiply() {
		int g = 650 ;
		int h = 10 ;
		int i = g * h;
		System.out.println(i);
	}
	public void divide () {
		int j = 450 ;
		int k = 50 ;
		int l = j / k ;
		System.out.println(l);
	}
	
	
	
}
