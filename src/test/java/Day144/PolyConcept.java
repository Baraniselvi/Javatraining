package Day144;

public class PolyConcept {
	
	public int add(int a , int b) {
		int c = a + b;
		System.out.println(c);
		return c;
		
	}
	
	public int add(int a , int b , int d) {
		int c = a + b+d;
		System.out.println(c);
		return c;

}
	
	public int add(int a , int b ,int d ,int z) {
		int c = a + b +d +z;
		System.out.println(c);
		return c;
	}
}
